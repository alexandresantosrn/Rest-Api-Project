package com.restapi.userdpt.entities;

public class Departament {

	private Long id;
	private String name;

	public Departament() {
		super();
	}

	public Long getId() {
		return id;
	}

	public void setId(Long id) {
		this.id = id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

}
